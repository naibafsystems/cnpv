  CREATE OR REPLACE FUNCTION "CNP_INSCRIPCION_WEB"."FC_CALCULASEMANAANTERIOR" RETURN VARCHAR2 AS 
 p_fecha date := sysdate;
 v_fecha date ;
 v_dia   number := 0;
BEGIN
  select p_fecha-7 into v_fecha from dual;
  dbms_output.put_line (to_char(v_fecha));
  select to_number(to_char (v_fecha , 'D')) into  v_dia from dual;
  dbms_output.put_line (v_dia);
  while v_dia > 1 loop
     v_fecha := v_fecha - 1  ;
     select to_number(to_char (v_fecha , 'D')) into  v_dia from dual;
  END LOOP;
  dbms_output.put_line (to_char(v_fecha + 1));
  dbms_output.put_line (to_char(v_fecha + 5));
  RETURN (to_char(v_fecha + 1)||','||to_char(v_fecha + 5));
END FC_CALCULASEMANAANTERIOR;

